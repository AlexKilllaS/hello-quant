# 【策略逻辑说明】
# ============================================================
# 一、核心思想
#   多因子打分：提取价格动量因子与成交额因子，分别进行排名后合成综合得分。
#   动量因子：用近N日涨幅衡量，涨得多的说明短期动能更强。
#   成交额因子：用近N日平均成交额衡量，成交额越大说明流动性越好。
#
# 二、备选池
#   5只不同方向的高流动性ETF，覆盖科技成长、周期、消费、避险：
#     - 588000.SH  科创50ETF（科技成长）
#     - 512880.SH  证券ETF（周期风向标）
#     - 159928.SZ  消费ETF（稳定价值）
#     - 518880.SH  黄金ETF（避险）
#     - 511010.SH  国债ETF（避险）
#
# 三、调仓频率
#   每周五 14:50 检查一次。
#
# 四、买入规则
#   1. 计算所有标的的近20日涨幅与近20日均成交额。
#   2. 分别对涨幅和均成交额进行降序排名。
#   3. 将两个因子的排名相加得到综合得分，按综合得分升序排列（得分越小越靠前）。
#   4. TOP_N 参数控制选前几名，默认选2只等分买入。
#
# 五、卖出规则
#   持仓标的不再是综合得分前TOP_N时卖出。
#     ① 每周五14:50重新计算全排名
#     ② 如果当前持仓不在目标名单中，先全卖
#     ③ 再按新目标买入
#     ④ 先卖后买，中间隔1根1分钟K线（约1分钟）完成换仓
#
# 六、参数说明（在init中修改）
#   LOOKBACK_DAYS  = 20   因子回看周期（交易日）
#   REBALANCE_DAY  = 5    调仓日：5=周五
#   REBALANCE_TIME = '14:50'  调仓执行时间
#   TOP_N          = 2    选前N支等分买入
#   TOTAL_AMOUNT   = 10万 总投入金额（元）
#   MIN_LOTS       = 100  最小委托单位（100份起）
#   accountid      = 资金账号
#
# 七、回测设置
#   周期：1分钟K线（handlebar每1分钟触发一次）
#   数据复权：前复权（dividend_type='front_ratio'）
#   手续费：千1（可在set_commission调整）
#
